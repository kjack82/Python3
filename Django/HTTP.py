## HTTP stands for Hypertext Transfer Protocol
## Used to structure requests and responses over the internet. 
## Data transferred from one point to another 

## Example. Type in address to brower - this commans a TCP channel to the server that responds to URL. 
## The computer making request is called the client 
## The URL is the address that belongs to the server. 
## TCP connection established client sends HTTP GET request to sever to GET the webpage it shoud display. 
## Once response sent, connection closed. 
## New connection opened if a new request sent through. 

## HTTPS is secure, required when usually sensive data like credit card info or address etc. 

