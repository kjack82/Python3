## This means many forms 
## It refers to methods/functions/operators with same name that can be executed on many objects or classes

## example.... 

# len() function

#for a string 
x = "Hello World!"
print(len(x)) #prints 12 

#Tuple 
mytuple = ("apple", "banana", "cherry")
print(len(mytuple)) #prints 3 

#Dictionary
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}

print(len(thisdict)) #prints 3